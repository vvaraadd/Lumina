"""
Lumina Text Engine

5x7 bitmap font.
Each glyph is represented as a tuple of 7 rows,
where each row contains 5 pixels.

0 = pixel off
1 = pixel on
"""

FONT_WIDTH = 5
FONT_HEIGHT = 7
FONT_SPACING = 1

DEFAULT_CHARACTER = "?"

FONT = {}


def add(character, bitmap):
    """Register a glyph in the font."""

    if len(bitmap) != FONT_HEIGHT:
        raise ValueError(f"{character!r} must have {FONT_HEIGHT} rows.")

    for row in bitmap:
        if len(row) != FONT_WIDTH:
            raise ValueError(f"{character!r} rows must have {FONT_WIDTH} columns.")

    FONT[character] = bitmap


